package com.quiz.bean;

public class Login {

	String message;
	
	public Login() {
		super();
	}

	/**
	 * @param message
	 */
	public Login(String message) {
		super();
		this.message = message;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
	
	
}
